/**
 * The `ButtonClickAction` enum defines the default actions available for
 * buttons related to collections. The basic actions are Create, Edit, and
 * Delete.
 */
export enum ButtonClickAction {
    Create = 'create',
    Edit = 'edit',
    Delete = 'delete',
    Save = 'save'
}
